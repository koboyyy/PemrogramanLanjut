public class Percabangan2 {
    public static void main(String[] args) {
        int a = 15;
        int b = 23;

        System.out.println("Nilai a = " + a);
        if (a%5 == 0){
            System.out.println(a + " Habil dibagi 5");
        }else {
            System.out.println(a + " Tidak habis dibagi 5");
        }

        System.out.println("Nilai b = " + b);
        if (b%5 == 0){
            System.out.println(b + " Habil dibagi 5");
        }else {
            System.out.println(b + " Tidak habis dibagi 5");
        }
    }
}
